n = int(input())
# 뚜껑
print(('*'*n) + (' '*(3*n-3-n)) + ('*'*n))

# 위
for i in range(1, n-1):
    print((' '*i) + '*' + (' '*(n-2)) + '*' + (' '*(3*n-3-n-(i*2))) + '*' + (' '*(n-2)) + '*')

# 가운데 부분
print((' '*(n-1)) + '*' + (' '*(n-2)) + '*' + (' '*(n-2)) + '*')

# 아래
for i in range(n-2, 0, -1):
    print((' '*i) + '*' + (' '*(n-2)) + '*' + (' '*(3*n-3-n-(i*2))) + '*' + (' '*(n-2)) + '*')

# 바닥
print(('*'*n) + (' '*(3*n-3-n)) + ('*'*n))